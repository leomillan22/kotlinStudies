fun main(){
    val anos: Int = 2
    val meses: Int = anos*12
    val dias: Int = meses*30
    val horas: Int = dias*24
    val minutos: Int = horas*60
    val segundos: Int = minutos*60

    println("${anos} anos equivalem a:")
    println("${meses} meses")
    println("${dias} dias")
    println("${horas} horas")
    println("${minutos} minutos")
    println("${segundos} segundos")
}